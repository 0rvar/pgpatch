#[test]
fn primary_key_changed_emits_drop_then_add() {
    let sql = emit::sql(&[Change::PrimaryKeyChanged {
        table: qual("public", "t"),
        before: Index {
            definition: "PRIMARY KEY (id)".into(),
            unique: true,
            primary: true,
        },
        after: Index {
            definition: "PRIMARY KEY (id, tenant_id)".into(),
            unique: true,
            primary: true,
        },
    }]);
    let drop_idx = sql.find("DROP CONSTRAINT t_pkey").expect("drop missing");
    let add_idx = sql.find("ADD PRIMARY KEY (id, tenant_id)").expect("add missing");
    assert!(drop_idx < add_idx, "drop should come before add:\n{sql}");
    // Statements separated by a blank line (i.e. `\n\n`).
    assert!(sql.contains(";\n\nALTER TABLE public.t ADD PRIMARY KEY"));
}

