#[test]
fn column_changed_multiple_axes_emits_multiple_statements() {
    // Type change + nullable change + default change all at once.
    let before = col("c", "integer");
    let mut after = col("c", "bigint");
    after.nullable = false;
    after.default = Some("0".into());
    let sql = emit::sql(&[Change::ColumnChanged {
        table: qual("public", "t"),
        name: "c".into(),
        before,
        after,
    }]);
    // All three statements emitted, joined by `\n` within the same bucket entry.
    assert!(sql.contains("ALTER COLUMN c TYPE bigint"));
    assert!(sql.contains("ALTER COLUMN c SET NOT NULL"));
    assert!(sql.contains("ALTER COLUMN c SET DEFAULT 0"));
    // Ordering: type, then nullable, then default (matches code order).
    let t = sql.find("TYPE bigint").unwrap();
    let n = sql.find("SET NOT NULL").unwrap();
    let d = sql.find("SET DEFAULT 0").unwrap();
    assert!(t < n && n < d);
}

