#[test]
fn statements_separated_by_blank_line() {
    // Two TableRemoved should produce two statements separated by an empty
    // line, both terminated with a trailing newline.
    let sql = emit::sql(&[
        Change::TableRemoved { qual: qual("public", "a") },
        Change::TableRemoved { qual: qual("public", "b") },
    ]);
    assert_eq!(sql, "DROP TABLE public.a;\n\nDROP TABLE public.b;\n");
}

#[test]
fn drop_phase_runs_before_create_phase() {
    // DROP TABLE (drop_tables bucket) must precede ADD COLUMN (create_columns
    // bucket) regardless of input order.
    let mut c = col("x", "integer");
    c.nullable = false;
    let sql = emit::sql(&[
        Change::ColumnAdded { table: qual("public", "t"), column: c },
        Change::TableRemoved { qual: qual("public", "old") },
    ]);
    let drop_idx = sql.find("DROP TABLE public.old").unwrap();
    let add_idx = sql.find("ADD COLUMN x").unwrap();
    assert!(drop_idx < add_idx, "drop phase should precede create phase:\n{sql}");
}

