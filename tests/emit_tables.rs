// Tests for the SQL emitter, focused on TABLE, COLUMN, and PRIMARY KEY change
// variants. Other variants (indexes, constraints, views, triggers, policies,
// etc.) are covered elsewhere — except where they ride along inside a
// `TableAdded` payload, since the emitter buckets those into other phases.

use pgpatch::diff::Change;
use pgpatch::emit;
use pgpatch::model::{
    Column, Constraint, Identity, Index, Policy, QualifiedName, Table, Trigger,
};
use std::collections::BTreeMap;

fn qual(schema: &str, name: &str) -> QualifiedName {
    QualifiedName::new(schema, name)
}

fn col(name: &str, ty: &str) -> Column {
    Column {
        name: name.into(),
        data_type: ty.into(),
        nullable: true,
        default: None,
        identity: None,
        generated: None,
        collation: None,
        comment: None,
    }
}

#[test]
fn table_added_plain() {
    let mut t = Table::default();
    t.columns = vec![col("id", "integer"), col("name", "text")];
    let sql = emit::sql(&[Change::TableAdded { qual: qual("public", "users"), table: t }]);
    let expected = "\
CREATE TABLE public.users (
    id integer,
    name text
);
";
    assert_eq!(sql, expected);
}

#[test]
fn table_added_with_primary_key_inline() {
    let mut t = Table::default();
    t.columns = vec![{
        let mut c = col("id", "integer");
        c.nullable = false;
        c
    }];
    t.primary_key = Some(Index {
        definition: "PRIMARY KEY (id)".into(),
        unique: true,
        primary: true,
    });
    let sql = emit::sql(&[Change::TableAdded { qual: qual("public", "t"), table: t }]);
    assert!(sql.contains("CREATE TABLE public.t ("));
    assert!(sql.contains("    id integer NOT NULL"));
    assert!(sql.contains("    PRIMARY KEY (id)"));
    // The PK clause is appended after columns, separated by a comma.
    assert!(sql.contains("NOT NULL,\n    PRIMARY KEY (id)\n);"));
}

#[test]
fn table_added_columns_preserve_vec_order() {
    let mut t = Table::default();
    // Insert in a non-alphabetical order to prove vec order is preserved.
    t.columns = vec![col("zeta", "text"), col("alpha", "text"), col("mu", "text")];
    let sql = emit::sql(&[Change::TableAdded { qual: qual("public", "t"), table: t }]);
    let z = sql.find("zeta").unwrap();
    let a = sql.find("alpha").unwrap();
    let m = sql.find("mu").unwrap();
    assert!(z < a && a < m, "columns should appear in vec order, got:\n{sql}");
}

#[test]
fn column_decl_emits_not_null_default_identity_generated_collation_in_order() {
    // Single column carrying every attribute exercised by column_decl.
    let mut c = col("c", "integer");
    c.nullable = false;
    c.default = Some("0".into());
    c.identity = Some(Identity::Always);
    c.generated = Some("c2 + 1".into()); // pathological, but exercises the code path
    c.collation = Some("C".into());

    let mut t = Table::default();
    t.columns = vec![c];
    let sql = emit::sql(&[Change::TableAdded { qual: qual("public", "t"), table: t }]);

    // Expected order: <name> <type> NOT NULL DEFAULT 0 GENERATED ALWAYS AS IDENTITY GENERATED ALWAYS AS (...) STORED COLLATE "C"
    let line = sql
        .lines()
        .find(|l| l.contains("c integer"))
        .expect("column line not found");
    let nn = line.find("NOT NULL").unwrap();
    let def = line.find("DEFAULT 0").unwrap();
    let id = line.find("GENERATED ALWAYS AS IDENTITY").unwrap();
    let g = line.find("GENERATED ALWAYS AS (c2 + 1) STORED").unwrap();
    let coll = line.find("COLLATE \"C\"").unwrap();
    assert!(nn < def && def < id && id < g && g < coll, "wrong order: {line}");
}

