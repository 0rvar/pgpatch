#[test]
fn column_changed_nullable_set_and_drop() {
    // nullable: true → false ⇒ SET NOT NULL
    let before = col("c", "integer");
    let mut after = col("c", "integer");
    after.nullable = false;
    let sql = emit::sql(&[Change::ColumnChanged {
        table: qual("public", "t"),
        name: "c".into(),
        before,
        after,
    }]);
    assert!(sql.contains("ALTER COLUMN c SET NOT NULL"));

    // nullable: false → true ⇒ DROP NOT NULL
    let mut before = col("c", "integer");
    before.nullable = false;
    let after = col("c", "integer");
    let sql = emit::sql(&[Change::ColumnChanged {
        table: qual("public", "t"),
        name: "c".into(),
        before,
        after,
    }]);
    assert!(sql.contains("ALTER COLUMN c DROP NOT NULL"));
}

