#[test]
fn table_added_buckets_constraints_indexes_triggers_policies_separately() {
    let mut t = Table::default();
    t.columns = vec![{
        let mut c = col("id", "integer");
        c.nullable = false;
        c
    }];
    t.constraints.insert(
        "t_chk".into(),
        Constraint { kind: "CHECK".into(), definition: "CHECK (id > 0)".into() },
    );
    t.indexes.insert(
        "t_idx".into(),
        Index {
            definition: "CREATE INDEX t_idx ON public.t (id)".into(),
            unique: false,
            primary: false,
        },
    );
    t.triggers.insert(
        "t_trg".into(),
        Trigger {
            definition: "CREATE TRIGGER t_trg BEFORE INSERT ON public.t FOR EACH ROW EXECUTE FUNCTION f()".into(),
        },
    );
    t.policies.insert(
        "t_pol".into(),
        Policy {
            command: "ALL".into(),
            permissive: true,
            roles: vec!["PUBLIC".into()],
            qual: Some("true".into()),
            with_check: None,
        },
    );
    t.rls_enabled = true;

    let sql = emit::sql(&[Change::TableAdded { qual: qual("public", "t"), table: t }]);

    // The CREATE TABLE block doesn't carry the indexes/constraints/triggers/
    // policies inline — they're hoisted to their own buckets.
    let create_table_idx = sql.find("CREATE TABLE public.t").unwrap();
    let create_index_idx = sql.find("CREATE INDEX t_idx").unwrap();
    let add_constraint_idx = sql.find("ALTER TABLE public.t ADD CONSTRAINT t_chk").unwrap();
    let create_trigger_idx = sql.find("CREATE TRIGGER t_trg").unwrap();
    let create_policy_idx = sql.find("CREATE POLICY t_pol").unwrap();
    let enable_rls_idx = sql.find("ENABLE ROW LEVEL SECURITY").unwrap();

    // create_tables → create_columns → create_constraints → create_indexes →
    // create_views → create_triggers → create_policies → rls.
    assert!(create_table_idx < add_constraint_idx);
    assert!(add_constraint_idx < create_index_idx);
    assert!(create_index_idx < create_trigger_idx);
    assert!(create_trigger_idx < create_policy_idx);
    assert!(create_policy_idx < enable_rls_idx);

    // The CREATE TABLE block itself has no constraint/index/trigger/policy text.
    let table_block_end = sql[create_table_idx..].find(");").unwrap();
    let table_block = &sql[create_table_idx..create_table_idx + table_block_end];
    assert!(!table_block.contains("CHECK (id > 0)"));
    assert!(!table_block.contains("CREATE INDEX"));
    assert!(!table_block.contains("CREATE TRIGGER"));
    assert!(!table_block.contains("CREATE POLICY"));
}

